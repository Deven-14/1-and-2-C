#include<stdio.h>

int main()
{
    int n1, n2;
    //n1 and n2 are the two numbers which are being added
    printf("Program to add two numbers\nEnter Number 1: ");
    scanf("%d", &n1);
    printf("Enter Number 2: ");
    scanf("%d", &n2);
    printf("Sum of %d and %d = %d\n",n1, n2, n1+n2);
    return 0;
}
  
